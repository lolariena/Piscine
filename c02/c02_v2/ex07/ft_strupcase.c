char	*ft_strupcase(char *str)
{
	int	i;

	if (!str)
		return (0);
	i = 0;
	while (str[i])
	{
		if (str[i] < 123 && str[i] > 96)
			str[i] -= 32;
		i++;
	}
	return (str);
}
