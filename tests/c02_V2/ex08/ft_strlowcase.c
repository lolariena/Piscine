char	*ft_strlowcase(char *str)
{
	int	i;

	if (!str)
		return (0);
	i = 0;
	while (str[i])
	{
		if (str[i] < 91 && str[i] > 64)
			str[i] += 32;
		i++;
	}
	return (str);
}
